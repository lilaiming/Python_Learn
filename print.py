def fibonacci(n):
    # 基本情况
    if n == 0:
        return 0
    elif n == 1:
        return 1
    # 递归步骤
    else:
        return fibonacci(n - 1) + fibonacci(n - 2)

# 调用函数
print(fibonacci(6))  # 输出: 8









































































































































































































































































































































































































































































